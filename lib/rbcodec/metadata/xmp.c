bool get_xmp_metadata(int fd, struct mp3entry* id3)
{
    /* TODO: Ugly temporary metadata. */
    /* At least, we need proper title and length. */
    id3->bitrate = 706;
    id3->frequency = 44100;
    id3->length = 180*1000;
    id3->vbr = false;
    id3->filesize = filesize(fd);
    
    return true;
}
